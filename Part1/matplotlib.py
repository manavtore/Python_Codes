import matplotlib.pyplot as plt

# 1. Plot lines and/or markers
plt.plot()

# 2. Create a scatter plot with markers
plt.scatter()

# 3. Generate vertical bar plots
plt.bar()

# 4. Generate horizontal bar plots
plt.barh()

# 5. Plot histograms
plt.hist()

# 6. Create a box-and-whisker plot
plt.boxplot()

# 7. Generate a pie chart
plt.pie()

# 8. Display an image on the axes
plt.imshow()

# 9. Plot contours
plt.contour()

# 10. Add a colorbar to a plot
plt.colorbar()

# 11. Add a horizontal line across the axis
plt.axhline()

# 12. Add a vertical line across the axis
plt.axvline()

# 13. Add a horizontal span (rectangle) across the axis
plt.axhspan()

# 14. Add a vertical span (rectangle) across the axis
plt.axvspan()

# 15. Add text to the axes
plt.text()

# 16. Set the label for the x-axis
plt.xlabel()

# 17. Set the label for the y-axis
plt.ylabel()

# 18. Set the title of the plot
plt.title()

# 19. Place a legend on the axes
plt.legend()

# 20. Display grid lines on the plot
plt.grid()

# 21. Set the x-axis tick locations
plt.xticks()

# 22. Set the y-axis tick locations
plt.yticks()

# 23. Set the x-axis limits
plt.xlim()

# 24. Set the y-axis limits
plt.ylim()

# 25. Create a new figure
plt.figure()

# 26. Create a figure and a set of subplots
plt.subplots()

# 27. Save the current figure to a file
plt.savefig()

plt.show()  # This is not a specific function but used to display the plot
